get a repo content: https://api.github.com/repos/{owner}/{repo}/contents
get a raw file with a scpecific commit: https://raw.githubusercontent.com/{owner}/{repo}/{commit_hash}/{filename}
get all commit: https://api.github.com/repos/repos/{owner}/{repo}/commits
get all commit of a file: https://api.github.com/repos/{owner}/{repo}/commits?path={path}